namespace AdventOfCode.Helpers;

internal class CoordinateInt
{
    public CoordinateInt(int x, int y)
    {
        X = x;
        Y = y;
    }

    public int X { get; }
    public int Y { get; }
}

internal class CoordinateLong
{
    public CoordinateLong(long x, long y)
    {
        X = x;
        Y = y;
    }

    public long X { get; }
    public long Y { get; }
}
